#include <bits/stdc++.h>
using namespace std;

int main()
{
    int seconds = 365 * 24 * 60 * 60;

    cout << "1 year consists of " << seconds << " seconds." << endl;
    cout << "2 years consists of " << seconds * 2 << " seconds." << endl;
    cout << "5 years consists of " << seconds * 5 << " seconds." << endl;
    cout << "10 years consists of " << seconds * 10 << " seconds." << endl;
}